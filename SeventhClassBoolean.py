# bool() method: It allows you to evaluate any value and give you True or False in return.
print(bool("Hello"))
print(bool(15))
print(bool("abc"))
print(bool(123))
print(bool(["apple","cherry","banana"]))

# False Values
print(bool(False))
print(bool(None))
print(bool(0))
print(bool(""))
print(bool(()))
print(bool([]))
print(bool({}))